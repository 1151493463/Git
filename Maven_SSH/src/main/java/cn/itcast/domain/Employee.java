package cn.itcast.domain;

import java.util.Date;

public class Employee {
	
	private Integer employeeId;
	private String employeeName;
	private String employeePassword;
	private String employeeImg;
	private String employeeAddress;
	private String employeeRegistCode;
	private Date employeeBrithday;
	private Date joinDate;
	
	
	public String getEmployeeRegistCode() {
		return employeeRegistCode;
	}

	public void setEmployeeRegistCode(String employeeRegistCode) {
		this.employeeRegistCode = employeeRegistCode;
	}

	public String getEmployeeAddress() {
		return employeeAddress;
	}

	public void setEmployeeAddress(String employeeAddress) {
		this.employeeAddress = employeeAddress;
	}

	private Department department;

	public Integer getEmployeeId() {
		return employeeId;
	}

	public void setEmployeeId(Integer employeeId) {
		this.employeeId = employeeId;
	}

	public String getEmployeeName() {
		return employeeName;
	}

	public void setEmployeeName(String employeeName) {
		this.employeeName = employeeName;
	}

	public String getEmployeePassword() {
		return employeePassword;
	}

	public void setEmployeePassword(String employeePassword) {
		this.employeePassword = employeePassword;
	}

	public String getEmployeeImg() {
		return employeeImg;
	}

	public void setEmployeeImg(String employeeImg) {
		this.employeeImg = employeeImg;
	}

	public Date getEmployeeBrithday() {
		return employeeBrithday;
	}

	public void setEmployeeBrithday(Date employeeBrithday) {
		this.employeeBrithday = employeeBrithday;
	}

	public Date getJoinDate() {
		return joinDate;
	}

	public void setJoinDate(Date joinDate) {
		this.joinDate = joinDate;
	}

	public Department getDepartment() {
		return department;
	}

	public void setDepartment(Department department) {
		this.department = department;
	}
	
	
}
